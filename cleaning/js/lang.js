const switchLanguageButton = document.getElementById("switch-language");

switchLanguageButton.addEventListener("click", function() {
  const currentLang = document.documentElement.lang;
  const nextLang = currentLang === "en" ? "fr" : "en";
  document.documentElement.lang = nextLang;
  loadLanguageContent();
});

function loadLanguageContent() {
    const lang = document.documentElement.lang;
    const elements = document.querySelectorAll('[data-language]');
    
    for (let i = 0; i < elements.length; i++) {
      const key = elements[i].dataset.language;
      elements[i].textContent = languages[lang][key];
    }
    document.cookie = `lang=${lang}; SameSite=Lax`;
}

function getLanguagePreference() {
    const cookies = document.cookie.split("; ");
    console.log(cookies)
    for (const cookie of cookies) {
      const [name, value] = cookie.split("=");
      if (name === "lang") {
        return value;
      }
    }
    return null;
}

const preferredLanguage = getLanguagePreference();
if (preferredLanguage) {
    document.documentElement.lang = preferredLanguage;
} else {
    document.documentElement.lang = "fr";
}
